/*-----------------------------------------------------------------------------------------------*/
/* Includes                                                                                      */
/*-----------------------------------------------------------------------------------------------*/
#include "esp32/Esp32Adc.h"

/*-----------------------------------------------------------------------------------------------*/
/* Public methods                                                                                */
/*-----------------------------------------------------------------------------------------------*/
/**************************************************************************************************
  * @brief      Constructor for ESP32 ADC
  * @return     Nothing
  ********************************************************************************************** */
Esp32Adc::Esp32Adc(uint8_t pin) {
  this->pin = pin;
}

/**************************************************************************************************
  * @brief      Setup ADC pin
  * @return     true if setup successful
  ********************************************************************************************** */
bool Esp32Adc::setup() {
  if (this->pin >= 0) {
    pinMode(this->pin, INPUT);
    analogReadResolution(12);  // ESP32 has 12-bit ADC
    return true;
  }
  return false;
}

/**************************************************************************************************
  * @brief      Read ADC value
  * @return     ADC reading
  ********************************************************************************************** */
bool Esp32Adc::read(uint16_t& value) {
  if (this->pin >= 0) {
    value = analogRead(this->pin);
    return true;
  }
  return false;
} 