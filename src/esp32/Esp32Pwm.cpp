/*-----------------------------------------------------------------------------------------------*/
/* Includes                                                                                      */
/*-----------------------------------------------------------------------------------------------*/
#include "esp32/Esp32Pwm.h"

/*-----------------------------------------------------------------------------------------------*/
/* Public methods                                                                                */
/*-----------------------------------------------------------------------------------------------*/
/**************************************************************************************************
  * @brief      Constructor for ESP32 PWM
  * @return     Nothing
  ********************************************************************************************** */
Esp32Pwm::Esp32Pwm(uint8_t pin) {
  this->pin = pin;
}

/**************************************************************************************************
  * @brief      Setup PWM pin
  * @return     true if setup successful
  ********************************************************************************************** */
bool Esp32Pwm::setup() {
  pinMode(this->pin, OUTPUT);
  return true;
}

/**************************************************************************************************
  * @brief      Write PWM duty cycle
  * @return     true if write successful
  ********************************************************************************************** */
bool Esp32Pwm::write(uint8_t dutyCycle) {
  if (this->pin >= 0) {
    analogWrite(this->pin, dutyCycle);
    return true;
  }
  return false;
} 