/*-----------------------------------------------------------------------------------------------*/
/* Includes                                                                                      */
/*-----------------------------------------------------------------------------------------------*/
#include "stm32/Stm32Pwm.h"

/*-----------------------------------------------------------------------------------------------*/
/* Public methods                                                                                */
/*-----------------------------------------------------------------------------------------------*/
/**************************************************************************************************
  * @brief      Constructor for STM32 PWM
  * @return     Nothing
  ********************************************************************************************** */
Stm32Pwm::Stm32Pwm(uint8_t pin) {
  this->pin = pin;
}

/**************************************************************************************************
  * @brief      Setup PWM pin
  * @return     true if setup successful
  ********************************************************************************************** */
bool Stm32Pwm::setup() {
  pinMode(this->pin, OUTPUT);
  return true;
}

/**************************************************************************************************
  * @brief      Write PWM duty cycle
  * @return     true if write successful
  ********************************************************************************************** */
bool Stm32Pwm::write(uint8_t dutyCycle) {
  if (this->pin >= 0) {
    analogWrite(this->pin, dutyCycle);
    return true;
  }
  return false;
} 