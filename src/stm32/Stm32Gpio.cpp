/*-----------------------------------------------------------------------------------------------*/
/* Includes                                                                                      */
/*-----------------------------------------------------------------------------------------------*/
#include "stm32/Stm32Gpio.h"

/*-----------------------------------------------------------------------------------------------*/
/* Public methods                                                                                */
/*-----------------------------------------------------------------------------------------------*/
/**************************************************************************************************
  * @brief      Constructor for STM32 GPIO
  * @return     Nothing
  ********************************************************************************************** */
Stm32Gpio::Stm32Gpio(uint8_t pin, uint8_t mode) {
  this->pin = pin;
  this->mode = mode;
}

/**************************************************************************************************
  * @brief      Setup GPIO pin
  * @return     true if setup successful
  ********************************************************************************************** */
bool Stm32Gpio::setup() {
  pinMode(this->pin, this->mode);
  return true;
}

/**************************************************************************************************
  * @brief      Write to GPIO pin
  * @return     true if write successful
  ********************************************************************************************** */
bool Stm32Gpio::write(uint8_t state) {
  if (this->pin >= 0 && this->mode == OUTPUT) {
    digitalWrite(this->pin, state);
    return true;
  }
  return false;
}

/**************************************************************************************************
  * @brief      Read from GPIO pin
  * @return     true if read successful
  ********************************************************************************************** */
bool Stm32Gpio::read(uint8_t& state) {
  if (this->pin >= 0 && this->mode == INPUT) {
    state = digitalRead(this->pin);
    return true;
  }
  return false;
} 