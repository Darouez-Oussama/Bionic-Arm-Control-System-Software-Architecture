/*-----------------------------------------------------------------------------------------------*/
/* Includes                                                                                      */
/*-----------------------------------------------------------------------------------------------*/
#include "Stm32Adc.h"

/*-----------------------------------------------------------------------------------------------*/
/* Public methods                                                                                */
/*-----------------------------------------------------------------------------------------------*/
/**************************************************************************************************
  * @brief      Constructor for the STM32 ADC
  * @return     Nothing
  ********************************************************************************************** */
Stm32Adc::Stm32Adc(uint8_t pin) {
  this->pin = pin;
}

/**************************************************************************************************
  * @brief      Setup the ADC
  * @return     true if setup successful
  ********************************************************************************************** */
bool Stm32Adc::setup() {
  pinMode(this->pin, INPUT);
  analogReadResolution(12);
  return true;
}

/**************************************************************************************************
  * @brief      Read ADC value
  * @return     ADC reading
  ********************************************************************************************** */
bool Stm32Adc::read(uint16_t& value) {
  if (this->pin >= 0) {
    value = analogRead(this->pin);
    return true;
  }
  return false;
} 