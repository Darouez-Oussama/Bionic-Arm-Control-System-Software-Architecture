#ifndef IGPIO_H 
#define IGPIO_H 

/*-----------------------------------------------------------------------------------------------*/
/* Includes                                                                                      */
/*-----------------------------------------------------------------------------------------------*/
#include <Arduino.h>

/*-----------------------------------------------------------------------------------------------*/
/* Classes                                                                                       */
/*-----------------------------------------------------------------------------------------------*/
class IGpio {
 
public:
  virtual ~IGpio() = default;
  virtual bool setup() = 0;
  virtual bool write(uint8_t state) = 0;
  virtual bool read(uint8_t& state) = 0;
};

#endif // IGPIO_H 